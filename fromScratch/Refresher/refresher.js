function refresher() {
  var element = document.getElementById("refresher");
  element.style.color = "pink";
  element.style.fontSize = "90px";
  element.innerHTML = "This is the onclick event";
}
function mouseover() {
  var element = document.getElementById("refresher");
  element.innerHTML = "This is the mouseover event";
  element.style.color = "blue";
  element.style.fontSize = "40px";
}
